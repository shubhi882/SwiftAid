// Helper function to get condition from symptoms
export const getConditionFromSymptoms = (text, language) => {
  const conditions = Object.keys(medicalSearchPatterns);
  let maxScore = 0;
  let likelyCondition = null;

  conditions.forEach(condition => {
    const patterns = medicalSearchPatterns[condition].patterns[language] || [];
    const highConfidenceTerms = medicalSearchPatterns[condition].confidence.highConfidence[language] || [];
    
    let score = 0;
    let confidenceTermsFound = 0;

    // Check for pattern matches
    patterns.forEach(pattern => {
      if (text.toLowerCase().includes(pattern.toLowerCase())) {
        score += 1;
      }
    });

    // Check for high confidence combinations
    highConfidenceTerms.forEach(term => {
      if (text.toLowerCase().includes(term.toLowerCase())) {
        confidenceTermsFound += 1;
      }
    });

    // Boost score if multiple high confidence terms are found together
    if (confidenceTermsFound >= 2) {
      score *= 1.5;
    }

    if (score > maxScore) {
      maxScore = score;
      likelyCondition = condition;
    }
  });

  return {
    condition: likelyCondition,
    confidence: maxScore > 2 ? 'high' : maxScore > 1 ? 'medium' : 'low',
    severity: likelyCondition ? medicalSearchPatterns[likelyCondition].severity : null
  };
};
